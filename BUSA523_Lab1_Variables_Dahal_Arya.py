import math

# Create variables x and y
x = 3
y = 12

# Multiply x and y, and store the result in z
z = x * y

# Divide z by 4, and store the result in SRoot
SRoot = z / 4

# Take the square root of SRoot
SRoot = math.sqrt(SRoot)

# Print SRoot
print(SRoot)


